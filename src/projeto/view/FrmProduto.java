    private void BtExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtExcluirActionPerformed
        Produtos prod = new Produtos();
        prod.setId(Integer.parseInt(TxtCodigo.getText()));
        ProdutosDAO dao = new ProdutosDAO();
        dao.deletarProduto(prod);

        new Utilitarios().limpaTela(PnCadProd);
    }//GEN-LAST:event_BtExcluirActionPerformed

    private void BtPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtPesquisarActionPerformed

        String nome = TxtDescricao.getText();
        Produtos prod = new Produtos();
        ProdutosDAO dao = new ProdutosDAO();

        prod=dao.consultaProdutoNome(nome);
        CbFornecedor.removeAllItems();

        if(prod.getDescricao()!=null){
        TxtCodigo.setText(String.valueOf(prod.getId()));
        TxtDescricao.setText(prod.getDescricao());
        TxtPreco.setText(String.valueOf(prod.getPreco()));
        TxtEstoque.setText(String.valueOf(prod.getQtdEstoque()));
        Fornecedores f = new Fornecedores();
        FornecedorDAO fdao = new FornecedorDAO();
        
        f= fdao.consultaFornNome(prod.getFornecedor().getNome());
        CbFornecedor.getModel().setSelectedItem(f);
        }else{
            JOptionPane.showMessageDialog(null, "Produto não encontrado");
        }
        
        
    }//GEN-LAST:event_BtPesquisarActionPerformed

