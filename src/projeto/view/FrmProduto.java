    private void TbProdutosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TbProdutosMouseClicked
        PnDadosProd.setSelectedIndex(0);
        TxtCodigo.setText(TbProdutos.getValueAt(TbProdutos.getSelectedRow(), 0).toString());
        TxtDescricao.setText(TbProdutos.getValueAt(TbProdutos.getSelectedRow(), 1).toString());
        TxtEstoque.setText(TbProdutos.getValueAt(TbProdutos.getSelectedRow(), 2).toString());
        TxtPreco.setText(TbProdutos.getValueAt(TbProdutos.getSelectedRow(), 3).toString()); 
        Fornecedores f = new Fornecedores();
        FornecedorDAO dao = new FornecedorDAO();
        f = dao.consultaFornNome(TbProdutos.getValueAt(TbProdutos.getSelectedRow(), 4).toString());
        CbFornecedor.removeAllItems();
        CbFornecedor.getModel().setSelectedItem(f);
        
    }//GEN-LAST:event_TbProdutosMouseClicked

