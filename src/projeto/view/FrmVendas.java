    private void TxtCodigoItemVendaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_TxtCodigoItemVendaKeyPressed
        if(evt.getKeyCode() == KeyEvent.VK_ENTER){
            Produtos prod = new Produtos();
            ProdutosDAO dao = new ProdutosDAO();
            
            prod = dao.buscaCdodigo(Integer.parseInt(TxtCodigoItemVenda.getText()));
            
            TxtProdutoVenda.setText(prod.getDescricao());
            TxtPUnitariVenda.setText(String.valueOf(prod.getPreco()));
        }
    }//GEN-LAST:event_TxtCodigoItemVendaKeyPressed

